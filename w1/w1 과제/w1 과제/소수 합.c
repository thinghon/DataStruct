#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
    int n, total = 0;  // 입력 받을 숫자 n과 합계를 저장할 변수 total 초기화
    printf("숫자 입력(2 < n < 10000): ");  // 사용자에게 숫자 입력을 요청
    scanf("%d", &n);  // 사용자로부터 숫자 입력 받기

    // n이 2보다 크고 10000보다 작은 값인지 확인하는 조건문
    if (n <= 2 || n >= 10000) {
        printf("(2 < n < 10000) 조건에 맞지 않음.\n");  // 조건에 맞지 않으면 오류 메시지 출력
        return 1;  // 조건에 맞지 않으면 프로그램 종료
    }

    // n부터 2까지 내려가면서 소수를 판별하는 반복문
    for (int i = n; i > 1; --i) {
        // 각 숫자에 대해 1부터 (i-1)까지 나누어 보면서 소수 여부를 판별
        for (int j = i - 1; j > 0; --j) {
            if (j == 1) {  // j가 1일 때, 소수라는 뜻 (나누어떨어지는 수가 없다는 의미)
                if (i % 10 == 1)  // 숫자 i가 1로 끝나는지 확인
                    total += i;  // 1로 끝나는 소수라면 total에 추가
            }
            else if (i % j == 0) {  // i가 j로 나누어 떨어지면 소수가 아니므로
                break;  // 나누어 떨어지면 더 이상 확인할 필요 없으므로 반복문 종료
            }
        }
    }

    printf("총합 : %d", total);  // 소수 판별 후 총합을 출력
    return 0;  // 프로그램 종료
}
